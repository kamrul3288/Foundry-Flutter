class AppIconSizes {
  AppIconSizes._();

  /// 12px — micro indicators, status dots inside dense UI.
  static const double indicator = 12;

  /// 16px — inline with body text, chip/tag leading icons, input affixes.
  static const double inline = 16;

  /// 20px — inline with titles, small buttons, dense toolbars.
  static const double dense = 20;

  /// 24px — toolbar actions, list item leading, standard buttons.
  /// The Material default and the "if in doubt" size.
  static const double standard = 24;

  /// 32px — prominent list items, large buttons, card headers.
  static const double prominent = 32;

  /// 48px — feature icons, empty states, large avatars.
  static const double feature = 48;

  /// 64px — hero illustrations, onboarding.
  static const double hero = 64;

  /// 96px — splash screens, full-page empty states.
  static const double display = 96;
}
